def format_pairs_list(pairs: list[list[str, str]]) -> str:
    result = "<b>–°–µ–≥–æ–¥–Ω—è –∏–≥—Ä–∞—é—Ç:</b>\n\n"
    order = 1
    for p1, p2 in pairs:
        result += f"{order}. {p1} <b>VS</b> {p2}\n"
        order += 1
    return result
